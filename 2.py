"""5. Write a program that prompts the user to enter a list of elements separated by spaces. The program should then remove any duplicates from the list and display the resulting list.
(Solve this problem by using a function to remove duplicates)
"""

def remove_duplicates(input_list):
    unique_elements = []

    for element in input_list:
        if element not in unique_elements:
            unique_elements.append(element)

    return unique_elements

user_input = input("Enter a list of elements separated by spaces: ")

input_list = user_input.split()

result_list = remove_duplicates(input_list)

print(f"{result_list}")

