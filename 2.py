"""2.Take a certain number from the user and calculate the Fibonacci sequence up to that point and print the sequence as a list.
(Solve this problem by using a function to calculate the Fibonacci sequence)
"""

def fibonacci_sequence(n):
    if n == 0:
        return []
    elif n == 1:
        return [0]

    fib_sequence = [0, 1]

    i = 2
    while i < n:
        fib_sequence = fib_sequence + [fib_sequence[-1] + fib_sequence[-2]]
        i += 1

    return fib_sequence

user_input = int(input("Enter the number of Fibonacci numbers to calculate: "))

result = fibonacci_sequence(user_input)
print(f"{result}")

