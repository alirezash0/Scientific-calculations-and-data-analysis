"""4. Write a program that prompts the user to enter a string. The program should then count and display the occurrences of each vowel (a, e, i, o, u) in the string.
(Solve this problem by using a function to count the vowels)
"""

def count_vowels(input_string):
    vowels = "aeiou"
    vowel_counts = {}

    for vowel in vowels:
        count = input_string.lower().count(vowel)
        vowel_counts[vowel] = count

    return vowel_counts

user_input = input("Enter a string: ")

vowel_counts = count_vowels(user_input)

print(f"{vowel_counts}")

